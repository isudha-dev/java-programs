package concepts.inheritance;

public class Parent {
    public int a1;
    public int a2;

    public Parent(int a1, int a2) {
        this.a1 = a1;
        this.a2 = a2;
    }

    public void m1() {
        System.out.println("Parent m1");
    }

    public void m2() {
        System.out.println("Parent m2");
    }

}
